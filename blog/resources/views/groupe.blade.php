            <h3>L'orchestre : ADAC LIVE</h3>

        ADAC Live est un orchestre de variétés, composé de plus de quarante musiciens (cuivres, guitares, basses, percussions, synthés…), d’une dizaine de chanteurs et choristes ainsi que des danseuses et danseurs issus des écoles de musique de l’association ADAC. Ils partagent tous la même passion, LA MUSIQUE. Les représentations de l’ADAC sont époustouflantes. Sur scène la troupe de 50 musiciens, chanteurs et danseurs, interprète avec perfection et avec un enthousiasme communicatif un répertoire de variété française et internationale mais également de musique de films et de grands standards façon cabaret. Deux heures de show qui ravissent à coup sur tous les publics qui ont eu la chance de les voir en concert.

        Après avoir effectué pendant plusieurs années de nombreuses tournées et concerts sur la côte basque et méditerranéenne, en 2000 et 2003 les élèves ont pris leur envol pour effectuer deux tournées au CANADA et en ITALIE!

        Depuis, le groupe continue de se produire et à ravir son public dans la région Midi Pyrénées.



         LE MATÉRIEL :

        L’orchestre est équipé d’une sonorisation et de matériel d’éclairage constamment perfectionné qui assurent des prestations de qualité professionnelle.
        Depuis 2009, l’achat d’un vidéoprojecteur a permis d’améliorer encore le visuel des spectacles.
        Depuis janvier 2014, l’orchestre dispose d’un camion (équipé d’un hayon), acheté d’occasion suffisamment vaste (22 m³) pour entreposer et transporter le matériel de l’orchestre.



        LES REPRÉSENTATIONS

        L’orchestre est à la fois, une « vitrine », une publicité vivante pour notre enseignement et tout simplement pour nos jeunes un formidable moyen de faire partager leur amour de la Musique.
        Au rythme d’une dizaine de prestations par an, ce sont ainsi plus de 200 représentations qui ont été données depuis la création de l’ensemble : concerts dans les écoles afin de faire connaître à la population les progrès de nos élèves et faire partager à tous les joies de la Musique, spectacles au profit d’oeuvres charitables (Secours Populaire, Téléthon, restos du cœur…) ou tout simplement pour le plaisir de jouer (Fête de la Musique, …)
        Depuis 2011, avec l’attribution d’une licence d’entrepreneur de spectacles, le rythme des spectacles s’accélère, et 16 concerts sont programmés durant la saison 2013/2014.
        Parallèlement la qualité se « professionnalise » : chorégraphies, lumières, vidéoprojections mettent en valeur une interprétation musicale de qualité.

