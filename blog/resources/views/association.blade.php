                Présentation

        L’idée de créer l’ADAC (Association pour le Développement des Arts dans le Comminges) est née de la rencontre, en 1989, de deux musiciens, Jacques LEROY, Président de l’école de Musique de CIADOUX, et de Patrick SALANEUVE, le but étant de permettre l’accès de tous à la Culture en regroupant dans une « Association intercantonale » (devenue depuis interdépartementale), les écoles de Musique en milieu rural de la circonscription de Saint-Gaudens.

        L’ADAC a donc été créée en septembre 1991 sous la forme juridique d’une association loi 1901. Elle regroupe aujourd’hui 5 écoles de musique (Clarac, Castelnau-Magnoac, Encausse-les-thermes, Boulogne-sur-Gesse, Montréjeau) situées dans un rayon de 60 km autour de SAINT-GAUDENS (Haute-Garonne).

        Elle dispose de l’agrément d’association de Jeunesse et d’Éducation Populaire depuis 1995.

        Près de 350 adhérents encadrés par 17 enseignants peuvent étudier une quinzaine d’instruments (piano, guitare, cuivres, percussions, accordéon, violon… etc.) et pratiquer la musique d’ensemble dans plusieurs ateliers (musique traditionnelle, jazz, rock…).
        En plus de 20 ans, ce sont ainsi près de 2500 élèves qui ont été initiés aux joies de la pratique musicale.

        Un orchestre de Variétés, fort d’une soixantaine d’éléments, vient concrétiser les efforts de tous, élèves, enseignants et bénévoles, en diffusant un spectacle de près de 2 heures dans les communes de la Haute-Garonne et des départements limitrophes.

